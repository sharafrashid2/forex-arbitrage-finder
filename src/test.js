const bellmanFord = (Adj, start) => {
    // initialization
    const distances = {};
    for (const key in Adj) {
      distances[key] = Infinity;
    }

    const parents = {};
    for (const key in Adj) {
      parents[key] = null;
    }

    distances[start] = 0;
    parents[start] = start;

    // constructing shortest paths in rounds
    const V = Object.keys(Adj).length;

    for (let i = 0; i < V-1; i++) {
      for (const u in Adj) {
        for (const v of Adj[u]) {
          if (distances[v[0]] > distances[u] + v[1]) {
            distances[v[0]] = distances[u] + v[1];
            parents[v[0]] = u;
          }
        }
      }
    }

    for (const u in Adj) {
      for (const v of Adj[u]) {
        if (distances[v[0]] > distances[u] + v[1]) {
            let curr = v[0];

            for (let i = 0; i < V; i++) {
                curr = parents[curr];
            }

            let cycle = [];
            vertex = curr;

            while (true) {
                cycle.push(vertex);
                if (vertex == curr && cycle.length > 1) {
                    break;
                }
                vertex = parents[vertex];
            }
            cycle = cycle.reverse();
            return cycle;
        }
      }
    }
    return null;
}

